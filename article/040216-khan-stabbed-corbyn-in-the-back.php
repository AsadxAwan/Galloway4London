Richmond Park MP Mr Goldsmith weighed in, describing Mr Khan as “too partisan” to work with the Government to be able to protect London’s police.

He told the audience: “The first line in the job description of a mayor is holding government to account but also getting a good deal from government. My record as a working MP shows that I can and I will.”<br><br>

Mr Galloway said Britain’s involvement in conflicts in the Middle East and  Islamophobia at home were the biggest multipliers of extremism. But Mr Khan, a Muslim, said responsibility for terrorist attacks lay with the terrorists alone and that the vast majority of British extremists were radicalised at home.

He told a woman in the audience who called for mosques to be shut down to prevent extremism that her remarks were “extremely offensive”.<br><br>

Mr Goldsmith “strongly” backed Mr Khan’s words but admitted the Government’s counter-extremism strategy had “failed to find the balance”. 

Housing, the key issue of the campaign, also dominated the debate. Ms Walker said she would give “first dibs” on  housing to victims of domestic abuse. 

Ms Pidgeon, who has said City Hall should set up its own housing company, said the next Mayor should continue the Olympic precept on council tax to pay for a huge increase in affordable house-building.<br><br> 

Mr Whittle called for a “golden age” of social housing with Londoners getting priority over new homes, while Ms Berry, who rents, said: “There will be an infinite demand for our homes if you’re not controlling who buys them. They will be snapped up by investors and then they will still be charging us the same amount.”</p>



