Mr Khan, who nominated Mr Corbyn but did not vote for him, has gone to great lengths to distance himself from the Labour leader, who is regarded by some as an electoral liability.<br><br>

He told the audience: “The job of a politician is to appeal to everyone, not just to your own tribe. One of my criticisms of the leadership of the Labour party is that we are talking to ourselves. You don’t change people’s lives by losing elections and you don’t change people’s lives by talking to yourself.”

