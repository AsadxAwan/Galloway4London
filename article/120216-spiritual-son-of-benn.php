Speaking in a coffee shop in Neasden, north-west London, minutes before taking to the streets to campaign, Galloway claimed some on Labour’s frontbench were even more left-wing than him.<br><br>

Galloway also responded to Labour MP Jess Phillips’s claim that he was a “rape apologist” after he spoke in defence of Wikileaks founder Julian Assange.<br><br>

Reflecting on Corbyn’s support for the UK staying the EU, Mr Galloway – wearing his trademark hat – said: “I think it was a very big mistake. Even tactically - it deprived Labour of the ability to cause difficulties for the Government which is usually one of the articles of faith of Her Majesty’s Opposition.<br><br>

