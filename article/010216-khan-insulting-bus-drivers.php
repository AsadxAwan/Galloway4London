<p align="justify">Labour also views it as a useful way of illustrating the gulf between their candidate and Tory rival Zac Goldsmith, whose father was a billionaire financier.

Mr Khan told the Sunday Times: “My dad was never embarrassed of being a bus driver, but it was clear, in a non-arrogant way, that he was better educated.

“My parents wanted, again not in an arrogant way, but they wanted their children to do better than them. I don’t mean this as a discourtesy to bus drivers. I’ve got friends who are bus drivers.

