<p align="justify" style="color:#000000;">Why have I joined GO? Because this question is the single biggest issue we face in this country today, and I want to nail the lie that to be in favour of a vote to Leave the European Union, is to be on the political right of this debate. Nothing could be further from the truth.</p>

<p align="justify" style="color:#000000;">In 1975 I campaigned alongside my leader, the late, great Tony Benn, against Britain joining the then European Common Market.  Then as now, we knew that this was a bosses club, designed to walk over the rights of workers and peoples. An undemocratic organisation staffed by expensive, unaccountable bureaucrats, to whom we would sacrifice control of our own affairs.</p>

<p align="justify" style="color:#000000;">The final straw for me, as I announced last year in a public meeting, was the crushing of Greece, despite the overwhelming vote of its people to reject EU mandated austerity. This made clear to me that despite pipe-dreams of “reforming” the European Union, it is a tool for ensuring neoliberal, corporate dominance in its member states.</p>

<p align="justify" style="color:#000000;">In fact, the economic policies of Jeremy Corbyn and John McDonnell would be illegal under EU legislation. It would be illegal to renationalise our rail franchises or other public services. To run a temporary deficit of over 3%. To impose tariffs on Chinese imports to counteract dumping and protect our steel industry. And if you think this is bad, wait until TTIP becomes a reality.</p>

<p align="justify" style="color:#000000;">Then there is Foreign Policy. I woke up one day to find out that a certain Catherine Ashton was now our EU Foreign Minister. After a lifetime in Parliament, mostly in the Labour Party to which she belongs, my first question was “who are you?” Yet Ms Ashton had the power to, and did, lead this country into a number of confrontations on behalf of the EU, with no scrutiny or debate in the British Parliament.</p>

<p align="justify" style="color:#000000;">It is possible, indeed probable, that the current and recent British governments, would do all of these things. However, we can defeat them in Parliament, as happened with David Cameron’s insane drive to bomb Syria in August 2013. We can change our government and elect new MP’s. We can do no such thing with the EU bureaucracy.</p><br>

