<p align="justify">He’s outraged at Khan’s “brutal” criticism of Jeremy Corbyn in recent interviews: “And I know that it hasn’t been popular of Corbyn’s circle,” he adds. “He called their economic policy not just ‘rubbish’ but ‘ridiculous’ and accused John McDonnell [the shadow chancellor] and Corbyn of condoning terrorism in London. You can’t get a more serious charge from a mayoral candidate of his own leader than that.”<br><br> 

All this opens “a huge swathe of voters to me. Millions do not regard Palestinians as terrorists, or Corbyn’s stand on Ireland as encouraging terrorism, or Corbyn’s economic policy as ridiculous. And millions are not much into swooning in front of the Queen holding the Koran in your left hand. This man is beatable. And if I come ahead of him I can win because many of his voters will put me as their second preference.”<br><br> 

Galloway and Corbyn are old comrades. Forty years ago they were protégés of the late Tony Benn, “our political father. We sat next to each other for most of those years. He’s not a laugh-a-minute,” he says of Corbyn. “He’s a serious, earnest guy. But very lovable.”<br><br>  

There’s talk that Galloway would like to re-enter the Labour fold — he was expelled in 2003 for “bringing the party into disrepute” over his opposition to the Iraq war. Has he spoken to Corbyn? “I wouldn’t go public with what my communication has been with him,” he says, “but we’re in touch through others. I’ve no doubt my point of view is heard.”<br><br> 

One gripe is Corbyn’s appearance. “If I were dressing him he’d be a lot smarter,” Galloway says. “He’s scrubbed up a bit but the shorts were a fashion error.”<br><br> 

Clothes are important. “I try to look my best, notwithstanding this difficult early-morning start. My father was a factory worker but never went out not looking his best. People should put their best foot forward.”<br><br> 

Galloway spends a fortune on vintage clothes, buying from shops such as Victory in Whiteleys and Rokit in Covent Garden. The other day “an Arab admirer, a Saudi woman living in Holland Park” offered him £3,000 for his “trademark” fedora. He’s even bought the lease on his own vintage shop near Portobello (“which will open sometime” but is “a sore point” with his missus “because she didn’t want to”).<br><br>  

Zac Goldsmith dresses extremely well too,” he cackles, “and he’s very handsome and elegantly turned out, so we will be a fashion item in the election.”<br><br>  

He boasts about his eclectic political circle — friendships have included “Sir Peter Tapsell, the ex-[Tory] Father of the House — he was like a father to me. David Davis is like a brother and I’m very friendly with Andrew Mitchell.”<br><br> 

I didn’t expect him to be living like Gandhi exactly but his north London home is plush, with architectural glass doors and floating staircase. It’s rented from Willa Keswick, scion of the banking dynasty worth £1.75 billion, and I question whether this chimes with his view that bankers are evil “sharks” involved in “grand larceny”. He laughs: “Willa is a lovely girl despite being a socialite.”<br><br> 

Actually the whole set up is weirdly west London for a Marxist who spent years in Blackheath and Streatham protesting against the elite — and says he will stand in the Tooting parliamentary by-election if its MP Sadiq Khan wins the mayoral election. There’s a vintage Mercedes on the drive and two more cars in the road (“because with four young grandchildren we move around as 10. We’re a ‘table for 10, please’”).<br><br>  

He’s a regular at Soho House-owned Pizza East and a member of the Groucho Club. “I have been for 25 years — politicians were not technically allowed but Nigella Lawson and her late husband John Diamond were friends and as membership secretaries swung it for me I was the only politician there — probably still am”.<br><br> 

If his forthcoming film — The Killing of Tony Blair — makes money he hopes to buy a house in Camden, “a lovely area”.<br><br> 

“I don’t live a sackcloth and ashes life,” he says. “If you come from my background you appreciate nice things because you never had them. But I work for them.”</p><br>


