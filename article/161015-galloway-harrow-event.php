LONDON (16.10.2015) London Mayoral contender and RESPECT Party leader George Galloway was in Harrow on Wednesday night to present his campaign to a packed hall at South Harrow Baptist Church. Alongside him spoke Mukhtar Ghaffar, local community activist and RESPECT GLA candidate for Brent and Harrow.<br><br>
Mr.Galloway, who served as a UK Member of Parliament for over 25 years, spoke to the crowd about local, national and international issues, before answering questions from the floor.<br><br>
He told the audience: “Sadiq Khan has promised to be the most pro-business Mayor in London’s history. Well I will be the most pro-worker Mayor. The banks and big business don’t need any more powerful people on their side. The workers and the tenants and hard-pressed home owners do.</p><br>

