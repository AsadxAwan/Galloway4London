The notion of him returning to the party has already been floated by former Mayor Ken Livingstone, but many in Labour have strongly spoken out against Galloway coming back.

One of those was Birmingham Yardley MP Jess Phillips, who branded Galloway a “rape apologist” after claiming – in reference to the Julian Assange rape accusations – that “not everybody needs to be asked prior to each insertion."

The Huff Post UK asked Galloway whether he regretted making those comments.

He said: “I made them many times and I’m not minded to respond to Jess Philips, whose role in life seems to be to slander and insult people.”</p>


