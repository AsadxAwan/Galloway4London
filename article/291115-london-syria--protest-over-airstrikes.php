<p align="justify">Eno said later that he had attended in the hope that the demonstration would “make the government nervous that they can’t just steamroller over people’s feelings about this”.<br><br>

Many demonstrators looked back to the 2003 Stop The War protests, the biggest ever seen in Britain, but bemoaned Saturday’s low turnout. “Words can’t describe how disappointing it is for me. It’s heartbreaking,” said protester Martin Rosendaal, 78, the son of a refugee from the Nazis and a former soldier. “How many wars have I lived through to know when one is wrong? All my life we’ve been interfering in Arab wars and made the situation worse. There’s no question that what the terrorists are doing is disgraceful, and I’m no pacifist – but bombing will recruit for the terrorists, there’s no doubt about that.”<br><br>

After the speeches a letter to the prime minister was handed over at the gates to Downing Street, signed by 30 politicians and celebrities including Rylance, Eno, comedians Arthur Smith, Frankie Boyle and Francesca Martinez, film-maker Ken Loach and writer Michael Rosen. The letter calls on David Cameron to look for “political solutions” and points out that wars in Afghanistan, Iraq and Libya show that “western military interventions lead to large-scale casualties, devastating destruction and huge flows of refugees”. It claims this had led to a rise in terrorism, and asks the government to stop arming Saudi Arabia and Qatar, which fund such groups.<br><br>

