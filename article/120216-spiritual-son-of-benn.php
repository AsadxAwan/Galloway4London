<p align="justify">Setting out his own reasons why he is calling for the UK to leave the EU, Galloway said: “The Greece debacle, where a left wing government resisting austerity and the ruin of working people and their families in Greece, were elected and then crushed by the overwhelming power of a German-dominated European Central Bank and other financial instruments and organs of the EU and no matter that they voted and they voted again and they voted again they are crushed by that same austerity.”<br><br>

He added: “Mr Benn, who I campaigned with in 1975, made the peerless – then, as now – case.

“If the European Union could be reformed that would be one thing, but it structurally, architecturally, is designed in a way that it cannot be reformed.

“There is no democracy in the European Union, the European Parliament is a joke.”

Galloway is one of the country’s most recognisible politicians, and through out the interview and his subsequent speech outside the London Fatwa Council, people approached and asked for photographs.<br><br>

The Scottish-born former Labour MP admits that part of his notoriety is down to his appearance on Celebrity Big Brother in 2006.

As well as performing a dance routine while wearing a skin-tight red leotard, he also memorably asked fellow contestant Rula Lenska: “Would you like me to be the cat?” before miming licking a saucer of milk.

When asked by the Huff Post UK if he regretted appearing on the show, and specifically the “cat” incident, Galloway replied: “I don’t actually. That was a decade again so it’s a bit thin gruel as a criticism now.

“I did it for charity, people do all kinds of things for charity and it’s usually approved of - when Jeremy Vine dressed up in women’s lingerie.

“It’s generally thought to be a good thing, but some people want to use it against me.

“But the truth is, that last man I took a picture with said ‘I’ve been following you since I was a child’ and the first thing he cited was Big Brother, so for a generation of people it’s not a bad thing to have been in it.<br><br>

“I wouldn’t do it again.”

