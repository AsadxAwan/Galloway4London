<p align="justify">“The baby’s been up all night with a fever,” he explains in his Dundee growl, “I overslept.” He mutters praise for Calpol and asks me to wait on the ground floor of his cavernous home while he freshens up.<br><br> 

Plenty of the firebrand’s fans would be weak-kneed to meet Galloway, 61, in a state of such undress. The father-of-four (including children of eight, four, and 15-month-old Toren, with his fourth wife Gayatri) was nicknamed “Gorgeous George” for his reputation with the ladies, although I gather he’s rather shy on the subject of “exes”. <br><br> 

Anyway, I’m here because the former Respect Party MP for Bradford West is currently polling third in the mayoral race — behind Conservative MP Zac Goldsmith and Labour’s Sadiq Khan — on what sounds like a flamboyant platform, proposing “to run Uber out of town” and “cause conflict with the bankers of the City”.<br><br> 

But then Galloway loves controversy. He declared Bradford “an Israel-free zone” and his campaign against Labour’s Naz Shah (who won by 11,420 votes) in May was so vicious each reported the other’s party to the police. Shah calls him a “Spandex cat” (referencing his appearance on Celebrity Big Brother) while Galloway accused Shah of lying about her marriage, saying she was 16 not 15 when it took place —arguably a minor detail as it was forced. At the time he announced he would challenge the result “with a Section 106 complaint under the Representation of the People Act”, but now claims that he is unable to talk about it because the police are still investigating.<br><br> 

Opponents describe him variously as “vitriolic, misogynist and very dangerous”, and a “gut politician” whose skill at rhetoric is “pure proletarian poetry”. But he also has a loyal fanbase.<br><br> 

A wall of his political heroes eye me up in his hall: Che Guevara, Fidel Castro, Ho Chi Minh; a bad portrait of Yasser Arafat hangs next to Winston Churchill. There’s a bust of Lenin.<br><br>  

A housekeeper lets herself in and makes me a cup of tea. Then Galloway is back — fedora on — rubbing his hands. He’s “always fancied the job of mayor”, he says. “I’m not going to get votes that would otherwise go to Zac Goldsmith. So it’s me verses Sadiq Khan for the Centre-Left vote.” He gets stuck into Khan, a “flip-flop merchant” and “product of the Blairite machine” who is “not the Zeitgeist”, but an “unprincipled, speak-your-weight machine” politician who “went into what can only be described as a swoon over kissing the Queen’s hand”.<br></p>

